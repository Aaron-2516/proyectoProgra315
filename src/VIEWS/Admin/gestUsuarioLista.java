    private void iniciarActualizacionAutomatica() {
        autoRefreshTimer = new Timer(25000, e -> { // 25000 ms = 25 segundos
            System.out.println("üîÑ Actualizaci√≥n autom√°tica de lista de usuarios...");
            controller.cargarUsuarios();
        });
        autoRefreshTimer.start();
        System.out.println("‚úÖ Actualizaci√≥n autom√°tica iniciada (cada 25 segundos - lista usuarios)");
    }
    
    private void configurarAncestorListener() {
        this.addAncestorListener(new AncestorListener() {
            @Override
            public void ancestorAdded(AncestorEvent event) {
                System.out.println("üîç Panel agregado a la jerarqu√≠a - Actualizando tabla lista usuarios...");
                controller.cargarUsuarios();
            }
            
            @Override
            public void ancestorRemoved(AncestorEvent event) {
            }
            
            @Override
            public void ancestorMoved(AncestorEvent event) {
            }
        });
    }
    
